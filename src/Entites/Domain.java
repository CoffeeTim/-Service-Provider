package Entites;

/**
 *
 * @author MSI
 */
public class Domain {
    private int id ;
    private String title , color ;
  
    public Domain() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "Domain{" + "id=" + id + ", title=" + title + ", color=" + color +  "}\n ";
    }

}
